package beans;
public class HenkiloBean {
    private String nimi;
    private String osoite;
    private String kotikunta;
    private String email;
    
    public String getNimi() { return nimi; }
    public void setNimi(String nimi) { this.nimi = nimi; }
    
    public String getOsoite() { return osoite; }
    public void setOsoite(String osoite) { this.osoite = osoite; }
    
    public String getKotikunta() { return kotikunta; }
    public void setKotikunta(String kotikunta) { this.kotikunta = kotikunta; }
    
    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }
}